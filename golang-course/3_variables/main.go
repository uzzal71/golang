package main

import "fmt"

func main() {
	var name string = "golan"
	fmt.Println(name)

	var company = "infosys";
	fmt.Println(company)

	var isAdult = true
	fmt.Println(isAdult)

	var age int32 = 30
	fmt.Println(age)

	// shorthand syntax
	city := "Dhaka"
	fmt.Println(city)

	var country string
	country = "Bangladesh"
	fmt.Println(country)

	var price float32
	price = 50.6
	fmt.Println(price)

	var percentage = 5.8
	fmt.Println(percentage)

	discount := 10.2
	fmt.Println(discount)
}